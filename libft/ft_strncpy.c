#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*d;

	d = dst;
	len++;
	while ((len-- > 1) && (*(d++) = *(src++)))
		;
	while (len-- > 1)
		*(d++) = '\0';
	return (dst);
}
