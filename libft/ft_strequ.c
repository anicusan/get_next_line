#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	char	*d1;
	char	*d2;

	d1 = (char*)s1;
	d2 = (char*)s2;
	while (*d1 && *d2)
	{
		if (*d1 != *d2)
			return (0);
		d1++;
		d2++;
	}
	if (*d1 != *d2)
		return (0);
	return (1);
}
