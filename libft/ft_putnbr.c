#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb >= 10 || nb <= -10)
		ft_putnbr(nb / 10);
	if (nb < 0 && nb > -10)
	{
		ft_putchar('-');
	}
	nb = nb % 10;
	if (nb > 0)
		ft_putchar(nb + '0');
	else
		ft_putchar(-nb + '0');
}
